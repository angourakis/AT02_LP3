<br><table class="table">
  <article><h2>O que é a API Google Play Games Services?</h2>
		<p>É uma API (Application Program Interface) que permite a integração e visualização de: Conquistas, Estatísticas de Jogadores, 
		Salvar Jogos e Jogatina em Tempo Real entre diferentes dispositivos!<br>
		Veja algumas coisas do que você pode fazer com esta API logo abaixo:</p>
	</article>
  <tbody>
    <tr>
      <th>achievementDefinitions</th>
		<td>Lists all the achievement definitions for your application.</td>
	</tr>
	<tr>
      <th>achievements</th>
		<td>Updates multiple achievements for the currently authenticated player.</td>
	</tr>
	<tr>	
      <th>applications</th>
		<td>Indicate that the the currently authenticated user is playing your application.</td>
	</tr>
	<tr>
	<th>events</th>
		<td>Returns a list of the event definitions in this application.</td>
	</tr>
	<tr>
	  <th>leaderboards</th>
		<td>Lists all the leaderboard metadata for your application.</td>
	</tr>
	<tr>
	  <th>metagame</th>
		<td>Return the metagame configuration data for the calling application.</td>
	</tr>
	<tr>
	  <th>players</th>
		<td>Get the collection of players for the currently authenticated user.</td>
	</tr>
	<tr>
	  <th>pushtokens</th>
		<td>Registers a push token for the current user and application.</td>
	</tr>
	<tr>
	  <th>questMilestones</th>
		<td>Report that a reward for the milestone corresponding to milestoneId for the quest corresponding to questId has been claimed by the currently authorized user.</td>
	</tr>
	<tr>
	  <th>quests</th>
		<td>Get a list of quests for your application and the currently authenticated player.</td>
	</tr>
	<tr>
	  <th>revisions</th>
		<td>Checks whether the games client is out of date.</td>
	</tr>
	<tr>
	  <th>rooms</th>
		<td>Returns invitations to join rooms.</td>
	</tr>
	<tr>
	  <th>scores</th>
		<td>Submits multiple scores to leaderboards.</td>
	</tr>
	<tr>
	  <th>snapshots</th>
		<td>Retrieves a list of snapshots created by your application for the player corresponding to the player ID.</td>
	</tr>
	<tr>
	  <th>turnBasedMatches</th>
		<td>Returns turn-based matches the player is or was involved in.</td>
    </tr>
  </tbody>
</table>